package it.unibo.virtualCasino.model.games.impl.roulette.enums;

/**
 * The {@code RouletteColors} enum represents the colors associated with the
 * numbers in a game of roulette.
 */
public enum RouletteColors {

    /**
     * Represents the color red.
     */
    RED,

    /**
     * Represents the color black.
     */
    BLACK,

    /**
     * Represents the color green, typically for the zero.
     */
    GREEN
}
